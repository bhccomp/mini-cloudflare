    <h2>1. Overview</h2>
    <p>FirePhage sells subscription-based protection and traffic services. Because account setup, onboarding time, infrastructure reservation, and support effort may begin immediately after purchase, refunds are not automatically guaranteed.</p>

    <h2>2. Self-serve subscriptions</h2>
    <p>For standard self-serve plans purchased through Stripe, customers may cancel future renewal at any time. Cancellation stops future billing, but it does not automatically create a refund for charges that have already been processed.</p>

    <h2>3. Early-stage exceptions</h2>
    <p>If you were charged recently and FirePhage has not yet materially delivered the paid service for your site, you can contact FirePhage to request a review. FirePhage may choose to issue a full or partial refund where appropriate, especially in obvious setup-error or duplicate-charge cases.</p>

    <h2>4. Non-refundable cases</h2>
    <p>Refunds will generally not be provided for:</p>
    <ul>
        <li>partially used billing periods;</li>
        <li>changes in traffic volume, usage, or request overages after the service was already active;</li>
        <li>customer-side configuration mistakes after onboarding guidance was provided;</li>
        <li>account suspension caused by abuse, policy violations, or unpaid invoices;</li>
        <li>custom sales agreements that explicitly state different commercial terms.</li>
    </ul>

    <h2>5. Overage and usage billing</h2>
    <p>If your plan includes usage-based billing or request overages, those charges reflect measured traffic processed by FirePhage under the plan terms. If you believe a usage charge is incorrect, contact FirePhage promptly so it can be reviewed.</p>

    <h2>6. Manual and enterprise agreements</h2>
    <p>Custom plans, invoiced customers, and enterprise agreements may have separate commercial terms. Where a signed order form, proposal, or negotiated agreement exists, that agreement controls over this general refund policy.</p>

    <h2>7. How to request review</h2>
    <p>For refund review, billing disputes, or duplicate-charge questions, email <a href="mailto:billing@firephage.com">billing@firephage.com</a> with your organization name, billing email, site/domain, and a short description of the issue.</p>
