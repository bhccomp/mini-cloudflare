    <p><strong>Last updated:</strong> March 17, 2026</p>

    <h2>1. Purpose</h2>
    <p>FirePhage is designed to protect legitimate websites, applications, and online businesses. This policy exists to reduce abuse risk, protect other customers, and preserve trust with infrastructure and billing partners.</p>

    <h2>2. Prohibited use</h2>
    <p>You may not use FirePhage to support, conceal, accelerate, or defend:</p>
    <ul>
        <li>malware distribution, phishing, credential theft, or fraud;</li>
        <li>botnets, command-and-control activity, or unauthorized network attacks;</li>
        <li>illegal content or services where hosting, delivery, or protection would violate applicable law;</li>
        <li>spam operations, deceptive lead-generation systems, or large-scale abusive automation;</li>
        <li>activities intended to evade law enforcement, platform abuse review, or provider restrictions.</li>
    </ul>

    <h2>3. Security and platform misuse</h2>
    <p>You may not use FirePhage itself to test, interfere with, reverse engineer, overload, or disrupt the FirePhage platform, other customers, upstream providers, or related systems without explicit written permission.</p>

    <h2>4. Customer responsibility</h2>
    <p>You remain responsible for the content, applications, websites, DNS decisions, origin servers, and people using your FirePhage account. You must respond reasonably to abuse notices, billing issues, and support requests related to security risk on your sites.</p>

    <h2>5. Investigations and enforcement</h2>
    <p>FirePhage may investigate suspected violations of this policy. Where FirePhage believes a site, organization, or account creates legal, operational, abuse, payment, or security risk, FirePhage may limit features, suspend protection, disable access, or terminate service.</p>

    <h2>6. Urgent action</h2>
    <p>FirePhage may take immediate action without prior notice where necessary to respond to abuse, fraud, active attacks, legal demands, infrastructure-provider requirements, or security emergencies.</p>

    <h2>7. Reporting issues</h2>
    <p>If you need to report abuse involving a FirePhage-protected property, contact <a href="mailto:abuse@firephage.com">abuse@firephage.com</a> with relevant details, timestamps, domains, and evidence where possible.</p>
