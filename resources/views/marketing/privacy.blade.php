    <p><strong>Last updated:</strong> March 17, 2026</p>

    <h2>1. Overview</h2>
    <p>FirePhage processes account details, billing details, website configuration data, traffic telemetry, and WordPress security information in order to provide edge protection, dashboard visibility, onboarding help, and customer support.</p>

    <h2>2. Information we collect</h2>
    <p>Depending on how you use FirePhage, we may collect:</p>
    <ul>
        <li>Account information such as name, email address, organization membership, and login activity.</li>
        <li>Billing information such as billing email, Stripe customer details, subscription status, invoices, and payment metadata.</li>
        <li>Website information such as domains, DNS-related setup details, edge configuration, origin information, and plan selection.</li>
        <li>Traffic and security telemetry such as request logs, firewall decisions, traffic volume, countries, paths, status codes, and security-related events.</li>
        <li>WordPress plugin reports such as site health status, update exposure, checksum posture, malware scan summaries, and findings shared by the FirePhage plugin.</li>
    </ul>

    <h2>3. How we use information</h2>
    <p>FirePhage uses this information to:</p>
    <ul>
        <li>provide and secure the platform;</li>
        <li>protect websites and origins;</li>
        <li>support onboarding and DNS cutover assistance;</li>
        <li>measure plan usage and billing thresholds;</li>
        <li>send transactional emails, alerts, and operational notices;</li>
        <li>diagnose issues, improve performance, and develop the service.</li>
    </ul>

    <h2>4. Payments</h2>
    <p>Payments are handled through Stripe or another approved billing provider. FirePhage does not store full payment card details on its own servers. Billing providers process payment information under their own terms and privacy policies.</p>

    <h2>5. Sharing</h2>
    <p>FirePhage does not sell customer data. Information may be shared with service providers and infrastructure partners only where reasonably necessary to operate the platform, process billing, send emails, deliver support, or comply with legal obligations.</p>

    <h2>6. Data retention</h2>
    <p>FirePhage keeps data for as long as needed to operate the service, support customers, maintain security records, handle billing, and meet legal or contractual obligations. Retention periods may differ depending on the type of data involved.</p>

    <h2>7. Security</h2>
    <p>FirePhage uses administrative, technical, and operational safeguards intended to protect customer information. Even so, no system can guarantee absolute security, and customers remain responsible for their own endpoint, origin, WordPress, and account security practices.</p>

    <h2>8. Your choices</h2>
    <p>You can update account and billing details from the FirePhage dashboard. If you want account, organization, or data-related help, contact FirePhage directly. Some records may need to be retained for billing, fraud prevention, abuse handling, or legal compliance.</p>

    <h2>9. International processing</h2>
    <p>FirePhage and its providers may process information in different countries depending on where infrastructure, billing, email, and operational systems are located.</p>

    <h2>10. Cookies and similar technologies</h2>
    <p>FirePhage uses necessary cookies and similar browser-side storage for website security, session handling, CSRF protection, and remembering cookie preferences. FirePhage may also load Google Analytics in consent mode for website traffic measurement, with analytics storage denied by default unless you allow analytics cookies. See the <a href="{{ route('cookies') }}">Cookie Policy</a> for more detail.</p>

    <h2>11. Questions</h2>
    <p>If you have privacy questions or requests, contact <a href="mailto:privacy@firephage.com">privacy@firephage.com</a> or <a href="mailto:contact@firephage.com">contact@firephage.com</a>.</p>
